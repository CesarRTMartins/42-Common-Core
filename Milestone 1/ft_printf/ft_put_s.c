#include "ft_printf.h"

int	ft_put_s(char *arg)
{
	int	i;

	i = 0;
	if (!arg)
		arg = "(null)";
	while (arg[i])
	{
		ft_put_c(arg[i]);
		i++;
	}
	return (i);
}
